fn main() {
    let day_number = 15;

    match day_number {
        1 => println!("Sat"),
        2 => println!("Sun"),
        3 => println!("Mon"),
        4 => println!("Tue"),
        5 => println!("Wed"),
        6 => println!("Thu"),
        7 => println!("Fri"),
        _ => println!("Invalid day number"),
    }
}
